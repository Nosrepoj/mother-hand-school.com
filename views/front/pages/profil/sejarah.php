<h4> Dr. Maria Montessori </h4>
Maria Montessori , a physician and educator born in
Italy in 1870 ,is remembered as the founder of the
famous Montessori method of education which
emphasizes learning within mixed age groups in a
child-friendly setting .
She created a ’child-centered’ approach that
emphasizes rich environments, freedom within limits
, independence and respect for the student’s point
of view .
Montessori believed that humans are capable of
learning more at the stage of ( 0 to 6 )than at any
other . This was the period Montessori described as
the time of the absorbent mind; when young
children are capable of absorbing huge amounts of
information about the environment through their
senses .
